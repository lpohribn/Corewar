int		ft_atoi(const char *str)
{
	int			nbr;
	int			sign;
	const char	*cp = str;

	nbr = 0;
	sign = 1;
	while ((*cp > 8 && *cp < 14) || *cp == ' ')
		++cp;
	if (*cp == '-' || *cp == '+')
		sign = 44 - *cp++;
	while (*cp >= '0' && *cp <= '9')
		nbr = nbr * 10 + (*cp++ - '0');
	return (nbr * sign);
}
