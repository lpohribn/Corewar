double		ft_powd(double val, int pow)
{
	double		res;

	res = 1l;
	if (pow < 0)
		return (0);
	while (pow > 0)
	{
		if (pow & 1)
			res *= val;
		val *= val;
		pow >>= 1;
	}
	return (res);
}
